package hotspot.model;

import java.util.Date;

public class BaiVietInfor {
	private int idBaiViet;
	private String tenDiaDiem;
	private String huyenXa;
	private Date ngayVietBai;
	private String biDanh;
	private String moTa;
	private int soLuotThich;
	private String noiDung;
	private String tieuDe;
	
	public int getIdBaiViet() {
		return idBaiViet;
	}
	public void setIdBaiViet(int idBaiViet) {
		this.idBaiViet = idBaiViet;
	}
	public String getTenDiaDiem() {
		return tenDiaDiem;
	}
	public void setTenDiaDiem(String tenDiaDiem) {
		this.tenDiaDiem = tenDiaDiem;
	}
	
	public String getHuyenXa() {
		return huyenXa;
	}
	public void setHuyenXa(String huyenXa) {
		this.huyenXa = huyenXa;
	}
	public Date getNgayVietBai() {
		return ngayVietBai;
	}
	public void setNgayVietBai(Date ngayVietBai) {
		this.ngayVietBai = ngayVietBai;
	}
	public String getBiDanh() {
		return biDanh;
	}
	public void setBiDanh(String biDanh) {
		this.biDanh = biDanh;
	}
	public String getMoTa() {
		return moTa;
	}
	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}
	public int getSoLuotThich() {
		return soLuotThich;
	}
	public void setSoLuotThich(int soLuotThich) {
		this.soLuotThich = soLuotThich;
	}
	public String getNoiDung() {
		return noiDung;
	}
	public void setNoiDung(String noiDung) {
		this.noiDung = noiDung;
	}
	public String getTieuDe() {
		return tieuDe;
	}
	public void setTieuDe(String tieuDe) {
		this.tieuDe = tieuDe;
	}
	
}
